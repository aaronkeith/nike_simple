//RENDER GALLERY TITLE
function renderGalleryTitle(title, count) {

  var $galleryHeader = document.createElement('div')
  $galleryHeader.classList.add('gallery-title-bar')
  $galleryHeader.classList.add('col-xs-12')

  var $galleryTitle = document.createElement('span')
  $galleryTitle.classList.add('gallery-title')
  $galleryTitle.textContent = title

  var $galleryQty = document.createElement('span')
  $galleryQty.classList.add('gallery-shoe-qty')
  $galleryQty.textContent = '(' + count + ')'

  $galleryHeader.appendChild($galleryTitle)
  $galleryHeader.appendChild($galleryQty)

  return $galleryHeader
}

