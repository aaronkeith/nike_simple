//RENDER GALLERY FUNCTION
function renderGallery(title, count, shoes) {
  var $galleryContent = document.createElement('div')

  var $title = renderGalleryTitle(title, count)

  var $galleryItems = document.createElement('div')
  $galleryItems.classList.add('container')
  $galleryItems.classList.add('row')
  $galleryItems.setAttribute('id', 'gallery-items')

  for (var i = 0; i < shoes.length; i++) {
    var sneaker = renderShoe(shoes[i])
    $galleryItems.appendChild(sneaker)
  }

  $galleryContent.appendChild($title)
  $galleryContent.appendChild($galleryItems)

  return $galleryContent
}

